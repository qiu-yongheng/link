    /**
     * 计算CRC16
     */
    public static int crc16(byte[] data) {
        return calcCRC(data, 0, data.length);
    }

    /**
     * 计算CRC8
     */
    public static int crc8(byte[] data) {
        int crc8 = 0;

        for (byte datum : data) {
            int index = (crc8 ^ datum) & 255;
            crc8 = CRC8Table[index];
        }

        return crc8;
    }

    /**
     * CRC校验，看该数组是否满足CRC校验
     */
    public static boolean checkCRC16(byte[] data) {
        byte[] t = new byte[data.length - 2];
        System.arraycopy(data, 0, t, 0, data.length - 2);
        int crcInt = crc16(t);
        int len = data.length;
        int dataCrcInt = ((data[len - 2] & 0xFF) << 8) | (data[len - 1] & 0xFF);
        return dataCrcInt == crcInt;
    }

    /**
     * 对byte[] 校验CRC值。最前面是CRC16值。
     * byte[] 数组中前两位是 CRC16的值。后面的是发送的数据。
     */
    public static boolean checkFirstCRC16(byte[] data) {
        byte[] t = new byte[data.length - 2];
        System.arraycopy(data, 2, t, 0, data.length - 2);
        int crcInt = crc16(t);
        int dataCrcInt = ((data[0] & 0xFF) << 8) | (data[1] & 0xFF);
        if (dataCrcInt == crcInt) return true;
//        Log.d("CRC", "checkFirstCRC16()校验失败 目标: " + dataCrcInt + ", 当前: " + crcInt);
        return false;
    }

    /**
     * CRC8校验
     */
    public static boolean checkCRC8(byte[] data) {
        byte[] t = new byte[data.length - 1];
        System.arraycopy(data, 0, t, 0, data.length - 1);
        int crcInt = crc8(t);
        int len = data.length;
        int dataCrcInt = data[len - 1] & 0xFF;
        return dataCrcInt == crcInt;
    }

    private static int calcCRC(byte[] data, int offset, int len) {
        return calcCRC(data, offset, len, 0xffff);
    }

    private static int calcCRC(byte[] data, int offset, int len, int preval) {
        int ucCRCHi = (preval & 0xff00) >> 8;
        int ucCRCLo = preval & 0x00FF;
        int iIndex;
        for (int i = 0; i < len; i++) {
            iIndex = (ucCRCLo ^ data[offset + i]) & 0x00FF;
            ucCRCLo = ucCRCHi ^ t_crc16_h[iIndex];
            ucCRCHi = t_crc16_l[iIndex];
        }
        return ((ucCRCHi & 0x00FF) << 8) | (ucCRCLo & 0x00FF) & 0xFFFF;
    }
